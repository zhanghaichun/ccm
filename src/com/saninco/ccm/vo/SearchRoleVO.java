package com.saninco.ccm.vo;

import java.io.Serializable;

public class SearchRoleVO extends SearchVO implements Serializable {
	private static final long serialVersionUID = -4891774932870661027L;

	private Integer id;
	private String rname;
	private String isSave;
	private String fidArray;
	private String isAddOrDel;

	/*
	 * Getter And Setter
	 */
	public SearchRoleVO() {
	}

	public String getFidArray() {
		return fidArray;
	}

	public void setFidArray(String fidArray) {
		this.fidArray = fidArray.trim();
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getRname() {
		return rname;
	}

	public void setRname(String rname) {
		this.rname = rname.trim();
	}

	public String getIsSave() {
		return isSave;
	}

	public void setIsSave(String isSave) {
		this.isSave = isSave.trim();
	}

	public String getIsAddOrDel() {
		return isAddOrDel;
	}

	public void setIsAddOrDel(String isAddOrDel) {
		this.isAddOrDel = isAddOrDel.trim();
	}

	public String toString() {
		return "SearchRoleVO [fidArray=" + fidArray + ", id=" + id
				+ ", isAddOrDel=" + isAddOrDel + ", isSave=" + isSave
				+ ", rname=" + rname + "]";
	}

}
