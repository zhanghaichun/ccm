package com.saninco.ccm.model.bi;

public abstract class AbstractBIQuoteIssuedStatus implements java.io.Serializable {

	private static final long serialVersionUID = -2553298853713316433L;
	
	private Integer id;
	private String quoteStatusName;
	
	public AbstractBIQuoteIssuedStatus() {
	}

	/** full constructor */
	public AbstractBIQuoteIssuedStatus(String quoteStatusName) {
		
		this.quoteStatusName = quoteStatusName;
	}
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}

	public String getQuoteStatusName() {
		return quoteStatusName;
	}

	public void setQuoteStatusName(String quoteStatusName) {
		this.quoteStatusName = quoteStatusName;
	}
}
