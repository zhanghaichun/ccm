package com.saninco.ccm.model;

import java.sql.Timestamp;

public class ReceivedEmail extends AbstractReceivedEmail implements
		java.io.Serializable {
	// Constructors

	/**
	 * 
	 */
	private static final long serialVersionUID = -6405908906548104646L;

	/** default constructor */
	public ReceivedEmail() {
	}

	/** full constructor */
	public ReceivedEmail(AttachmentPoint attachmentPoint,
			String messageId, String fromAddress, String toAddress,
			String ccAddress, String bccAddress, String inReplyTo,
			String subject, String contentText, String contentHtml,
			String replySign, String msgReferences, String description,
			Timestamp sentDatetime, Timestamp createdTimestamp,
			Integer createdBy, Timestamp modifiedTimestamp, Integer modifiedBy,
			String recActiveFlag, String replyTo,String autoReplyFlag) {
		super(attachmentPoint, messageId, fromAddress, toAddress, ccAddress, bccAddress, inReplyTo,
				subject, contentText, contentHtml, replySign, msgReferences,
				description, sentDatetime, createdTimestamp, createdBy,
				modifiedTimestamp, modifiedBy, recActiveFlag, replyTo,autoReplyFlag);
	}

}
